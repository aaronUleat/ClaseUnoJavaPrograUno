package Ex2;

public class Vehiculos {

    public int moto = 0;
    public int livianos = 0;
     int pesados = 0;

    public void setMoto(int moto) {
        this.moto = moto;
    }

    public void setLivianos(int livianos) {
        this.livianos = livianos;
    }

    public void setPesados(int pesados) {
        this.pesados = pesados;
    }

    public int getMoto() {
        return moto;
    }

    public int getLivianos() {
        return livianos;
    }

    public int getPesados() {
        return pesados;
    }
}
